	/**
	 * Get the number of vertices (road intersections) in the graph
	 * @return The number of vertices in the graph.
	 */
	public int getNumVertices()
	{
		return myNodes.size();
	}
	
	/**
	 * Return the intersections, which are the vertices in this graph.
	 * @return The vertices in this graph as GeographicPoints
	 */
	public Set<GeographicPoint> getVertices()
	{
		Set<GeographicPoint> myVertices = new HashSet<>();
		for( MapNode node: myNodes){
			myVertices.add(node.getLocation());
		}
		return myVertices;
	}
	
	/**
	 * Get the number of road segments in the graph
	 * @return The number of edges in the graph.
	 */
	public int getNumEdges()
	{
		int totalNumEdges = 0;
		for( MapNode node: myNodes){
			totalNumEdges+=node.getNumberOfEdges();
		}
		return totalNumEdges;
	}

	
	
	/** Add a node corresponding to an intersection at a Geographic Point
	 * If the location is already in the graph or null, this method does 
	 * not change the graph.
	 * @param location  The location of the intersection
	 * @return true if a node was added, false if it was not (the node
	 * was already in the graph, or the parameter is null).
	 */
	public boolean addVertex(GeographicPoint location)
	{
		if((location==null) || (myMap.containsKey(location))){
			return false;
		}
		else{
			MapNode newNode = new MapNode(location);
			myNodes.add(newNode);
			myMap.put(location,newNode);
			return true;
		}
	}
	
	/**
	 * Adds a directed edge to the graph from pt1 to pt2.  
	 * Precondition: Both GeographicPoints have already been added to the graph
	 * @param from The starting point of the edge
	 * @param to The ending point of the edge
	 * @param roadName The name of the road
	 * @param roadType The type of the road
	 * @param length The length of the road, in km
	 * @throws IllegalArgumentException If the points have not already been
	 *   added as nodes to the graph, if any of the arguments is null,
	 *   or if the length is less than 0.
	 */
	public void addEdge(GeographicPoint from, GeographicPoint to, String roadName,
			String roadType, double length) throws IllegalArgumentException {
		if(from==null || to == null || roadName == null || roadType == null || length == 0.0 || length < 0){
			throw new IllegalArgumentException();
		}
		if(!myMap.containsKey(from)|| ! myMap.containsKey(to)){
			throw new IllegalArgumentException();
		}
		MapEdge newEdge = new MapEdge(from, to , roadName, roadType, length);
		myMap.get(from).addEdge(newEdge);
		myMap.get(to).addEdge(newEdge);
	}
	

